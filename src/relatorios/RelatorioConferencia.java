    private void jTextFieldMesaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldMesaKeyPressed
                         if(evt.getKeyCode() == KeyEvent.VK_ENTER){
           
           String mesa = jTextFieldMesa.getText();
        
          
           
            
         
          try {
              
              conecta.executaSQL("SELECT\n" +
"MESA,\n" +
"NOMEFUNC_PRE,\n" +
"PRODES_PRE,\n" +
"SUM(QUANTIDADE_PRE) AS QUANTIDADE_PRE\n" +
"FROM CONSUMO\n" +
"WHERE\n" +
"MESA='"+mesa+"' GROUP BY PRODES_PRE, MESA, NOMEFUNC_PRE");
              
            
                   HashMap filtro = new HashMap<>();
                
               filtro.put("mesa", mesa);
               filtro.put("$P{SUBREPORT_DIR}", true); 
               filtro.put("$P{REPORT_CONNECTION}", conecta ) ;      
                 
                  
                  JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs);
                  JasperPrint jpPrint = JasperFillManager.fillReport("relatorios_ireport/RelatorioConferencia.jasper",filtro, relatResul);
                  JasperViewer jv = new JasperViewer(jpPrint, false);
                  jv.setVisible(true);
                  
              
                  
              
          } catch (JRException ex) {
              System.out.println("Erro Relatorio: "+ex);  
          
                Logger.getLogger(Retaguarda.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("Erro no sql: "+ex);
                
            }
          
                    
                }
    }//GEN-LAST:event_jTextFieldMesaKeyPressed

