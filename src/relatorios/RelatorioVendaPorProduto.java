    private void jTextFieldCodProdutoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCodProdutoKeyPressed
      
         if(evt.getKeyCode() == KeyEvent.VK_ENTER){
             
             String numero = jTextFieldCodProduto.getText();
                final String ZEROS = "00000000000000";
                String s = String.valueOf(numero);
                String novoNumero = ZEROS.substring(s.length()) + s;   
                jTextFieldCodProduto.setText(novoNumero);
           
           conecta.executaSQL("SELECT * FROM produto WHERE procod='"+ jTextFieldCodProduto.getText()+"'");
           
           
             try {
                 if(conecta.rs.next()){
                     jLabelExibeDescricaoProduto.setText(conecta.rs.getString("prodesrdz"));
                 }else{
                     JOptionPane.showMessageDialog(rootPane, "Produto não cadastrado!");
                     jTextFieldCodProduto.setText("");
                     jLabelExibeDescricaoProduto.setText("");
                 }
             } catch (SQLException ex) {
                 Logger.getLogger(RelatorioVendaPorProduto.class.getName()).log(Level.SEVERE, null, ex);
             }
                    
                }
        
        
    }//GEN-LAST:event_jTextFieldCodProdutoKeyPressed

