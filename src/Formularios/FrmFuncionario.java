    private void jTextFieldCodColaboradorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCodColaboradorKeyPressed
        
         if(evt.getKeyCode() == KeyEvent.VK_ENTER){
          
             conecta.executaSQL("SELECT * FROM FUNCIONARIO WHERE FUNCOD='" + jTextFieldCodColaborador.getText()+"'" );
             try {
                 if(conecta.rs.next()){
                     jTextFieldNomeColaborador.setText(conecta.rs.getString("nome_funcionario"));
                     jTextFieldCpfColaborador.setText(conecta.rs.getString("cpf"));
                     jTextFieldEnderecoColaborador.setText(conecta.rs.getString("endereco"));
                     jTextFieldTelefoneColaborador.setText(conecta.rs.getString("telefone"));
                     
                     jButtonNovoColaborador.setEnabled(true);
                     jButtonEditarDados.setEnabled(true);
                     jButtonExcluirColaborador.setEnabled(true);
                     jButtonSalvarDados.setEnabled(false);
                     
                     
                     
                     
                 }
             } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(rootPane,"Erro ao pesquisar funcionário ou funcionário não cadastrado!  " + ex);
             }
             
             
         }
        
        
        
        
        
    }//GEN-LAST:event_jTextFieldCodColaboradorKeyPressed
