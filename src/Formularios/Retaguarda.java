    private void jCheckBoxVendaOnLineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBoxVendaOnLineActionPerformed
           
                
                atualizaVendaAutomatico(); 
        
		Timer timer = null;
		if (timer == null) {
			timer = new Timer();
			TimerTask tarefa = new TimerTask() {
				public void run() {
					try {
                                            if(jCheckBoxVendaOnLine.isSelected()){
						atualizaVendaAutomatico();
                                            }
					} catch (Exception e) {
						e.printStackTrace();
					}
				}
			};
			timer.scheduleAtFixedRate(tarefa, TEMPO, TEMPO);
		}	
	
                

          
    }//GEN-LAST:event_jCheckBoxVendaOnLineActionPerformed

