    private void jButtonAtualizarDadosVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarDadosVendasActionPerformed
        jCheckBoxVendaOnLine.setSelected(false);
        Date dataInicial = jDateChooserInicial.getDate();
        Date dataFinal = jDateChooserFinal.getDate();
        
       
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        
        conecta.executaSQL("SELECT SUM (TRNVLR) AS TOTAL FROM TRANSACAO WHERE TRNTIP='1' AND TRNDAT BETWEEN'"+ dataInicial+"' AND '"+ dataFinal+"'");
        
          try {
              while(conecta.rs.next()){
                  
                  
                  Float d = conecta.rs.getFloat("TOTAL");
        
                  jLabelExibeVendas.setText("R$  "+(df.format(d)));
                  
                  //jLabelExibeVendas.setText(Double.toString(df.format(d)));
                  
                  
              } } catch (SQLException ex) {
              Logger.getLogger(Retaguarda.class.getName()).log(Level.SEVERE, null, ex);
          }
         conecta.executaSQL("SELECT\n" +
"        SUM(\n" +
"            (B.ITVVLRTOT)-(A.PRECO_COMPRA_REAL*B.QUANTIDADE) )AS LUCROVALOR\n" +
"        FROM PRODUTO A \n" +
"            INNER JOIN ITEVDA B\n" +
"                ON A.PROCOD=B.PROCOD\n" +
"                INNER JOIN TRANSACAO C ON B.TRNSEQ=C.TRNSEQ\n" +
"               \n" +
"            WHERE B.TRNDAT BETWEEN'"+ dataInicial+"' AND '"+ dataFinal+"'\n" +
"            AND C.TRNTIP='1'");
        
          try {
              while(conecta.rs.next()){
                  
                  Float d13 = conecta.rs.getFloat("LUCROVALOR");
                  float perc = (d13*10)/100;
                  System.out.println(perc);
                  d13 = d13 + perc;
                  System.out.println(d13);
                  jLabelExibeLucro.setText("R$  "+df.format(d13));
                  
                  
              } } catch (SQLException ex) {
              Logger.getLogger(Retaguarda.class.getName()).log(Level.SEVERE, null, ex);
          }
        
        
    }//GEN-LAST:event_jButtonAtualizarDadosVendasActionPerformed

    
